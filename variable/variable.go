package main

import "fmt"

func main() {
	var name string

	name = "Bintang Gede"
	fmt.Println(name)

	name = "Bintang Hartono"
	fmt.Println(name)

	var friendName = "Javier"
	fmt.Println(friendName)

	var age = 18
	fmt.Println(age)

	country := "Indonesia"
	fmt.Println(country)

	country = "Jepang"
	fmt.Println(country)

	var (
		firstName = "Bintang Gede"
		lastName  = "Hartono"
	)

	fmt.Println(firstName)
	fmt.Println(lastName)
}
